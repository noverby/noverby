#!/bin/bash
set -euo pipefail

. "$(dirname "$0")/libtest-nix.sh"

setup_repo

# Verify the app is installed and has a files directory
assert_has_dir "$FL_DIR/app/org.test.Hello/$ARCH/stable/active/files"
ok "app installed with files/"

# Delete the metadata file to simulate corruption
rm -f "$FL_DIR/app/org.test.Hello/$ARCH/stable/active/metadata"
assert_not_has_file "$FL_DIR/app/org.test.Hello/$ARCH/stable/active/metadata"
ok "metadata deleted (simulated corruption)"

# Run repair — it should detect and regenerate the missing metadata
output=$($FLATPAK --user repair 2>&1)
echo "repair output: $output"

if echo "$output" | grep -q "broken.*metadata\|repaired.*metadata\|Repaired"; then
  ok "repair detected missing metadata"
else
  echo "FAIL: repair did not detect missing metadata"
  echo "Got: $output"
  exit 1
fi

# Verify metadata was regenerated
assert_has_file "$FL_DIR/app/org.test.Hello/$ARCH/stable/active/metadata"
ok "metadata regenerated by repair"

# Verify the app still appears in list
list_output=$($FLATPAK --user list 2>&1)
if echo "$list_output" | grep -q "org.test.Hello"; then
  ok "app still listed after repair"
else
  echo "FAIL: app not listed after repair"
  echo "Got: $list_output"
  exit 1
fi

echo "PASS: vm-repair-missing-file"