fn print_id(id: Id128, mode: PrintMode, value_only: bool, name: Option<&str>) {
    match mode {
        PrintMode::Pretty => {
            let label = name.unwrap_or("XYZ").to_uppercase().replace('-', "_");
            println!("As string:");
            println!("SD_ID128_CONST_STR(\"{}\")", id.to_uuid());
            println!("As macro:");
            println!("#define MESSAGE_{} SD_ID128_MAKE({})", label, id.to_make());
        }
        _ => {
            let formatted = id.format(mode);
            if value_only || name.is_none() {
                println!("{formatted}");
            } else {
                println!("{} {formatted}", name.unwrap());
            }
        }
    }
}

fn print_show_table(
    entries: &[(&str, Id128)],
    mode: PrintMode,
    value_only: bool,
    no_legend: bool,
    json_mode: JsonMode,
) {
    match json_mode {
        JsonMode::Off => {
            if mode == PrintMode::Pretty {
                for (i, (name, uuid)) in entries.iter().enumerate() {
                    if i > 0 {
                        println!();
                    }
                    let label = name.to_uppercase().replace('-', "_");
                    println!("/* {label} {} */", uuid.to_uuid());
                    println!(
                        "#define SD_ID128_CONST_STR_{label}  SD_ID128_MAKE_STR({})",
                        uuid.to_make()
                    );
                }
            } else if value_only {
                for (_, uuid) in entries {
                    println!("{}", uuid.format(mode));
                }
            } else {
                if !no_legend {
                    println!("NAME{:width$}ID", "", width = 28);
                }
                for (name, uuid) in entries {
                    println!("{name:<32}{}", uuid.format(mode));
                }
            }
        }
        _ => {
            let pretty = json_mode == JsonMode::Pretty;
            print_json(entries, mode, pretty);
        }
    }
}

#[derive(Clone, Copy, PartialEq, Eq)]
enum JsonMode {
    Off,
    Short,
    Pretty,
}

fn print_json(entries: &[(&str, Id128)], mode: PrintMode, pretty: bool) {
    let sep = if pretty { ",\n  " } else { "," };
    let start = if pretty { "[\n  " } else { "[" };
    let end = if pretty { "\n]" } else { "]" };

    let items: Vec<String> = entries
        .iter()
        .map(|(name, uuid)| {
            let id_str = uuid.format(if mode == PrintMode::Uuid {
                PrintMode::Uuid
            } else {
                PrintMode::Id128
            });
            if pretty {
                format!("{{\n    \"name\" : \"{name}\",\n    \"id\" : \"{id_str}\"\n  }}")
            } else {
                format!("{{\"name\":\"{name}\",\"id\":\"{id_str}\"}}")
            }
        })
        .collect();

    println!("{start}{}{end}", items.join(sep));
}

