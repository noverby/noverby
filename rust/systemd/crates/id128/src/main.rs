// ---------------------------------------------------------------------------
// Id128 type
// ---------------------------------------------------------------------------

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
struct Id128([u8; 16]);

macro_rules! id {
    ($($b:expr),+) => { Id128([$($b),+]) };
}

impl Id128 {
    fn is_null(self) -> bool {
        self.0 == [0; 16]
    }

    fn to_hex(self) -> String {
        self.0.iter().map(|b| format!("{b:02x}")).collect()
    }

    fn to_uuid(self) -> String {
        let h = self.to_hex();
        format!(
            "{}-{}-{}-{}-{}",
            &h[0..8],
            &h[8..12],
            &h[12..16],
            &h[16..20],
            &h[20..32]
        )
    }

    fn to_make(self) -> String {
        self.0
            .iter()
            .map(|b| format!("{b:02x}"))
            .collect::<Vec<_>>()
            .join(",")
    }

    fn format(self, mode: PrintMode) -> String {
        match mode {
            PrintMode::Id128 => self.to_hex(),
            PrintMode::Uuid => self.to_uuid(),
            PrintMode::Pretty => unreachable!(),
        }
    }

    fn from_hex(s: &str) -> Result<Self, String> {
        let clean: String = s.chars().filter(|c| *c != '-').collect();
        let clean = clean.trim();
        if clean.len() != 32 {
            return Err(format!(
                "Expected 32 hex characters, got {} in {:?}",
                clean.len(),
                s
            ));
        }
        let mut bytes = [0u8; 16];
        for i in 0..16 {
            bytes[i] = u8::from_str_radix(&clean[i * 2..i * 2 + 2], 16)
                .map_err(|e| format!("Invalid hex at position {}: {}", i * 2, e))?;
        }
        Ok(Id128(bytes))
    }

    fn make_v4_uuid(mut self) -> Self {
        self.0[6] = (self.0[6] & 0x0f) | 0x40; // version 4
        self.0[8] = (self.0[8] & 0x3f) | 0x80; // variant 1
        self
    }
}

#[derive(Clone, Copy, PartialEq, Eq)]
enum PrintMode {
    Id128,
    Uuid,
    Pretty,
}

// ---------------------------------------------------------------------------
// HMAC-SHA256
// ---------------------------------------------------------------------------

fn hmac_sha256(key: &[u8], data: &[u8]) -> [u8; 32] {
    const BLOCK_SIZE: usize = 64;

    let mut padded_key = [0u8; BLOCK_SIZE];
    if key.len() > BLOCK_SIZE {
        let hash = Sha256::digest(key);
        padded_key[..32].copy_from_slice(&hash);
    } else {
        padded_key[..key.len()].copy_from_slice(key);
    }

    let mut ipad = [0x36u8; BLOCK_SIZE];
    let mut opad = [0x5cu8; BLOCK_SIZE];
    for i in 0..BLOCK_SIZE {
        ipad[i] ^= padded_key[i];
        opad[i] ^= padded_key[i];
    }

    let inner = {
        let mut h = Sha256::new();
        h.update(ipad);
        h.update(data);
        h.finalize()
    };

    let outer = {
        let mut h = Sha256::new();
        h.update(opad);
        h.update(inner);
        h.finalize()
    };

    let mut out = [0u8; 32];
    out.copy_from_slice(&outer);
    out
}

fn derive_app_specific(base: Id128, app: Id128) -> Id128 {
    let hmac = hmac_sha256(&base.0, &app.0);
    let mut result = [0u8; 16];
    result.copy_from_slice(&hmac[..16]);
    Id128(result).make_v4_uuid()
}

