#[derive(Parser, Debug)]
#[command(
    name = "systemd-id128",
    about = "Generate and print 128-bit identifiers",
    version,
    disable_help_subcommand = true
)]
struct Cli {
    #[arg(
        short = 'p',
        long,
        global = true,
        help = "Generate samples of program code"
    )]
    pretty: bool,

    #[arg(
        short = 'P',
        long = "value",
        global = true,
        help = "Only print the value"
    )]
    value: bool,

    #[arg(short = 'u', long, global = true, help = "Output in UUID format")]
    uuid: bool,

    #[arg(
        short = 'a',
        long = "app-specific",
        global = true,
        value_name = "ID",
        help = "Generate app-specific IDs"
    )]
    app_specific: Option<String>,

    #[arg(
        long = "no-pager",
        global = true,
        help = "Do not pipe output into a pager"
    )]
    no_pager: bool,

    #[arg(
        long = "no-legend",
        global = true,
        help = "Do not show the headers and footers"
    )]
    no_legend: bool,

    #[arg(short = 'j', global = true, help = "Output in JSON format")]
    json_short: bool,

    #[arg(
        long = "json",
        global = true,
        value_name = "FORMAT",
        help = "Output in JSON (pretty, short, off)"
    )]
    json: Option<String>,

    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand, Debug)]
enum Command {
    /// Generate a new random 128-bit ID.
    New,

    /// Print the ID of current machine.
    #[command(name = "machine-id")]
    MachineId,

    /// Print the ID of current boot.
    #[command(name = "boot-id")]
    BootId,

    /// Print the ID of current invocation.
    #[command(name = "invocation-id")]
    InvocationId,

    /// Print the UUID for the /var/ partition.
    #[command(name = "var-partition-uuid")]
    VarPartitionUuid,

    /// Show well-known UUIDs.
    Show { args: Vec<String> },

    /// Show this help.
    #[command(name = "help")]
    ShowHelp,
}

fn die(msg: &str) -> ! {
    eprintln!("{msg}");
    std::process::exit(1);
}

fn main() {
    let cli = Cli::parse();

    let mode = if cli.pretty {
        PrintMode::Pretty
    } else if cli.uuid {
        PrintMode::Uuid
    } else {
        PrintMode::Id128
    };

    // -P overrides -p
    let mode = if cli.value && mode == PrintMode::Pretty {
        PrintMode::Id128
    } else {
        mode
    };

    let json_mode = if cli.json_short {
        JsonMode::Pretty
    } else if let Some(ref fmt) = cli.json {
        match fmt.as_str() {
            "pretty" => JsonMode::Pretty,
            "short" => JsonMode::Short,
            "off" => JsonMode::Off,
            _ => die(&format!("Unknown JSON format: {fmt}")),
        }
    } else {
        JsonMode::Off
    };

    let app_id = cli.app_specific.as_ref().map(|s| {
        if s.is_empty() {
            die("Application ID cannot be empty.");
        }
        let id = Id128::from_hex(s)
            .unwrap_or_else(|e| die(&format!("Failed to parse \"{s}\" as application-ID: {e}")));
        if id.is_null() {
            die("Application ID cannot be all zeros.");
        }
        id
    });

    match cli.command {
        Command::New => {
            let id = generate_random_id().unwrap_or_else(|e| die(&e));
            let id = match app_id {
                Some(app) => derive_app_specific(id, app),
                None => id,
            };
            print_id(id, mode, false, None);
        }

        Command::MachineId => {
            let id = read_machine_id().unwrap_or_else(|e| die(&e));
            let id = match app_id {
                Some(app) => derive_app_specific(id, app),
                None => id,
            };
            if mode == PrintMode::Pretty {
                print_id(id, mode, false, Some("machine-id"));
            } else {
                println!("{}", id.format(mode));
            }
        }

        Command::BootId => {
            let id = read_boot_id().unwrap_or_else(|e| die(&e));
            let id = match app_id {
                Some(app) => derive_app_specific(id, app),
                None => id,
            };
            if mode == PrintMode::Pretty {
                print_id(id, mode, false, Some("boot-id"));
            } else {
                println!("{}", id.format(mode));
            }
        }

        Command::InvocationId => {
            if app_id.is_some() {
                die("Verb \"invocation-id\" cannot be combined with --app-specific=.");
            }
            let id = read_invocation_id().unwrap_or_else(|e| die(&e));
            if mode == PrintMode::Pretty {
                print_id(id, mode, false, Some("invocation-id"));
            } else {
                println!("{}", id.format(mode));
            }
        }

        Command::VarPartitionUuid => {
            if app_id.is_some() {
                die("Verb \"var-partition-uuid\" cannot be combined with --app-specific=.");
            }
            let machine_id = read_machine_id().unwrap_or_else(|e| die(&e));
            let id = derive_app_specific(machine_id, GPT_VAR);
            if mode == PrintMode::Pretty {
                print_id(id, mode, false, Some("var-partition-uuid"));
            } else {
                println!("{}", id.format(mode));
            }
        }

        Command::Show { args } => {
            if args.is_empty() {
                if app_id.is_some() {
                    die("'show --app-specific=' can only be used with explicit UUID input.");
                }
                print_show_table(GPT_TABLE, mode, cli.value, cli.no_legend, json_mode);
            } else {
                let mut entries = Vec::new();
                for arg in &args {
                    if arg.is_empty() {
                        die(&format!("Unknown identifier \"{arg}\"."));
                    }
                    let (name, mut uuid) = gpt_resolve(arg).unwrap_or_else(|e| die(&e));
                    if let Some(app) = app_id {
                        uuid = derive_app_specific(uuid, app);
                    }
                    if cli.value && mode != PrintMode::Pretty {
                        println!("{}", uuid.format(mode));
                    } else {
                        let label = if name.is_empty() { arg.as_str() } else { name };
                        entries.push((label, uuid));
                    }
                }
                if !entries.is_empty() {
                    print_show_table(&entries, mode, cli.value, cli.no_legend, json_mode);
                }
            }
        }

        Command::ShowHelp => {
            println!("systemd-id128 [OPTIONS...] COMMAND\n");
            println!("Generate and print 128-bit identifiers.\n");
            println!("Commands:");
            println!("  new                     Generate a new ID");
            println!("  machine-id              Print the ID of current machine");
            println!("  boot-id                 Print the ID of current boot");
            println!("  invocation-id           Print the ID of current invocation");
            println!("  var-partition-uuid      Print the UUID for the /var/ partition");
            println!("  show [NAME|UUID]        Print one or more UUIDs");
            println!("  help                    Show this help\n");
            println!("Options:");
            println!("  -h --help               Show this help");
            println!("     --no-pager           Do not pipe output into a pager");
            println!("     --no-legend          Do not show the headers and footers");
            println!("     --json=FORMAT        Output in JSON (pretty, short, off)");
            println!("  -j                      Equivalent to --json=pretty");
            println!("  -p --pretty             Generate samples of program code");
            println!("  -P --value              Only print the value");
            println!("  -a --app-specific=ID    Generate app-specific IDs");
            println!("  -u --uuid               Output in UUID format");
        }
    }
}

