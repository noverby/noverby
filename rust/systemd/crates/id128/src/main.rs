fn gpt_find_by_uuid(uuid: Id128) -> Option<&'static str> {
    GPT_TABLE.iter().find(|(_, u)| *u == uuid).map(|(n, _)| *n)
}

fn gpt_find_by_name(name: &str) -> Option<Id128> {
    GPT_TABLE.iter().find(|(n, _)| *n == name).map(|(_, u)| *u)
}

fn gpt_resolve(name: &str) -> Result<(&'static str, Id128), String> {
    // Try parsing as UUID first
    if let Ok(uuid) = Id128::from_hex(name) {
        let canonical = gpt_find_by_uuid(uuid).unwrap_or("");
        return Ok((canonical, uuid));
    }
    // Try name lookup — find by name, then re-resolve by UUID for canonical name
    if let Some(uuid) = gpt_find_by_name(name) {
        let canonical = gpt_find_by_uuid(uuid).unwrap_or("");
        return Ok((canonical, uuid));
    }
    Err(format!("Unknown identifier \"{name}\"."))
}

